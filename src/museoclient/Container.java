    MuseoInt connect() {
        String nombreNodo;
        InputStreamReader ent = new InputStreamReader(System.in);
        BufferedReader buf = new BufferedReader(ent);
//        System.out.println("Introducir el nombre del nodo del registro RMI:");
//        nombreNodo = buf.readLine();
        nombreNodo = "localhost";
//        System.out.println("Introducir el nuÃÅmero de puerto del registro RMI:");
//        String numPuerto = buf.readLine();
        String numPuerto = "8881";
        
        String URLRegistro = "rmi://" + nombreNodo + ":" + numPuerto + "/holaMundo";
        MuseoInt h = null;
        try {
            h = (MuseoInt) Naming.lookup(URLRegistro);
        } catch (NotBoundException ex) {
            System.out.println(ex);
        } catch (MalformedURLException ex) {
            System.out.println(ex);
        } catch (RemoteException ex) {
            System.out.println(ex);
        }

        System.out.println("Busqueda completa, conectado ");

        return h;
    }

